#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t size)
{
	size_t	dest_len;
	size_t	src_len;

	dest_len = ft_strlen(dest);
	src_len = ft_strlen(src);
	if (size <= dest_len)
		return (size + src_len);
	while (*src && (--size > dest_len))
		dest++[dest_len] = *src++;
	dest[dest_len] = '\0';
	return (dest_len + src_len);
}
